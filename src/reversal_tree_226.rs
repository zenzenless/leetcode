use std::cell::RefCell;
use std::rc::Rc;
impl Solution {
    pub fn invert_tree(root: Option<Rc<RefCell<TreeNode>>>) -> Option<Rc<RefCell<TreeNode>>> {
        if let Some(node) = root.as_ref() {
            //let node = node.clone().borrow_mut();
            let mut t = node.borrow_mut();
            let tmp=t.left.clone();
            t.left=t.right.clone();
            t.right=tmp;
            Solution::invert_tree(t.left.clone());
            Solution::invert_tree(t.right.clone());
        }
        root
    }
}
// @lc code=end

struct Solution;
#[derive(Debug, PartialEq, Eq)]
pub struct TreeNode {
    pub val: i32,
    pub left: Option<Rc<RefCell<TreeNode>>>,
    pub right: Option<Rc<RefCell<TreeNode>>>,
}

impl TreeNode {
    #[inline]
    pub fn new(val: i32) -> Self {
        TreeNode {
            val,
            left: None,
            right: None,
        }
    }
}